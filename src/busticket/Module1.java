    public void showConductorTable() {
        try {
            Connection con = OracleConnection.getCon();
            String sql = "SELECT * FROM Conductor";
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel conductor = (DefaultTableModel) conductorTable.getModel();
            conductor.setRowCount(0);

            while (rs.next()) {
                Object o[] = {rs.getInt("conductorID"), rs.getString("firstName"), rs.getString("lastName"), rs.getString("tcNo"), rs.getString("contactNo"), rs.getString("dateOfBirth"), rs.getString("gender")};
                conductor.addRow(o);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

